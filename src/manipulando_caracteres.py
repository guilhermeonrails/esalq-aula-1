palavra = 'Universidade de São Paulo'
print(palavra[0:])
print(palavra[0:3]) # A notação [0:3] indica que você deseja extrair os caracteres da posição 0 (inclusivo) à posição 2 (exclusivo) da string.
print(palavra[16:])
print(palavra[-9:])
print(palavra[::-1])
print(palavra.split())
print(palavra.split()[2])
print('-'.join(palavra))